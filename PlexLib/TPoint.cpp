#include "TPoint.h"

void TPoint::SetX(double _x)
{
  x = _x;
}
//-------------------------------------------------------------------------------------------------

void TPoint::SetY(double _y)
{
 y = _y;
}
//-------------------------------------------------------------------------------------------------

double TPoint::GetX()
{
  return x;
}
//-------------------------------------------------------------------------------------------------

double TPoint::GetY()
{
  return y;
}
//-------------------------------------------------------------------------------------------------

TPoint::TPoint (double _x, double _y)
{
  x = _x;
  y = _y;
}
//-------------------------------------------------------------------------------------------------
TPoint::TPoint(TPoint &A)
{
  x = A.x;
  y = A.y;
}
//-------------------------------------------------------------------------------------------------
void TPoint::show()
{
	cout<<x<<"\t"<<y<<"\n";
}
//-------------------------------------------------------------------------------------------------
bool TPoint::operator == (TPoint &A)
{
	return ((x == A.GetX())&&(y == A.GetY()));
}
//-------------------------------------------------------------------------------------------------
TPoint& TPoint::operator = (TPoint &A)
{
	x = A.x;
	y = A.y;
	return *this;
}